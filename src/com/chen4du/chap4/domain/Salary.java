package com.chen4du.chap4.domain;

public class Salary {

	
	private String from;
	private String to;
	private double Amount;
	public String getFrom() {
		return from;
	}
	public String getTo() {
		return to;
	}
	public double getAmount() {
		return Amount;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public void setAmount(double amount) {
		Amount = amount;
	}
	
	
}
